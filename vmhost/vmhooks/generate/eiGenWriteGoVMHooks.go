package vmhooksgenerate

import (
	"fmt"
)

func eiGroupInterfaceName(group *EIGroup) string {
	return fmt.Sprintf("%sVMHooks", group.Name)
}

func WriteEIInterface(out *eiGenWriter, eiMetadata *EIMetadata) {
	autoGeneratedGoHeader(out, "executor")
	out.WriteString(`
// VMHooks contains all VM functions that can be called by the executor during SC execution.
type VMHooks interface {`)

	for _, group := range eiMetadata.Groups {
		out.WriteString(fmt.Sprintf("\n\t%s", eiGroupInterfaceName(group)))
	}

	out.WriteString("\n}\n")

	for _, group := range eiMetadata.Groups {
		out.WriteString(fmt.Sprintf("\ntype %s interface {\n", eiGroupInterfaceName(group)))

		for _, funcMetadata := range group.Functions {
			out.WriteString(fmt.Sprintf("\t%s(", upperInitial(funcMetadata.Name)))
			for argIndex, arg := range funcMetadata.Arguments {
				if argIndex > 0 {
					out.WriteString(", ")
				}
				out.WriteString(fmt.Sprintf("%s %s", arg.Name, vmHooksType(arg.Type)))
			}
			out.WriteString(")")
			if funcMetadata.Result != nil {
				out.WriteString(fmt.Sprintf(" %s", vmHooksType(funcMetadata.Result.Type)))
			}

			out.WriteString("\n")
		}

		out.WriteString("}\n")
	}

}

func externResult(functResult *EIFunctionResult) string {
	if functResult == nil {
		return "void"
	}
	return cgoType(functResult.Type)
}
